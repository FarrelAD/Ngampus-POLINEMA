/**
 * PercobaanIncrementDanDecrement
 */
public class PercobaanIncrementDanDecrement12 {

    public static void main(String[] args) {
        int F = 1;
        //increment
        System.out.println("F: " + F);
        System.out.println("++F: " + ++F);
        System.out.println("F++: " + F++);
        //decrement
        System.out.println("--F: " + --F);
        System.out.println("F--: " + F--);
        System.out.println("F--: " + F);
    }
}